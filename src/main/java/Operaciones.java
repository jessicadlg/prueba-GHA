public class Operaciones {

    public int sumar(int num1,int num2){
        return num1 + num2;
    }
    public int resta(int num1,int num2){
        return num1 - num2;
    }

    public int multiplicar(int num1, int num2) {
        return num1 * num2;
    }

    public int division(int num1, int num2) {
        return num1 / num2;

    }
}
